            Assert.That(result.HasHigherOrderStructure, Is.False);
            Assert.That(result.MonomersPerUnit, Is.EqualTo(1));
            Assert.That(result.MonomerCount, Is.EqualTo(0));
            Assert.That(result.HorCopyNumber, Is.EqualTo(0));
            Assert.That(result.MeanInterHorIdentity, Is.NaN);
            Assert.That(result.MeanIntraHorIdentity, Is.NaN);
        });
    }

    [Test]
    public void DetectHigherOrderRepeat_NullSequence_ReturnsNoStructure()
    {
        var result = ChromosomeAnalyzer.DetectHigherOrderRepeat(null!);

        Assert.Multiple(() =>
        {
            Assert.That(result.HasHigherOrderStructure, Is.False);
            Assert.That(result.MonomerCount, Is.EqualTo(0));
        });
    }

    [Test]
    public void DetectHigherOrderRepeat_FewerThanTwoMonomers_ReturnsNoStructure()
    {
        // One monomer (171 bp) cannot exhibit any periodicity.
        string oneMonomer = Monomer(SubsA);

        var result = ChromosomeAnalyzer.DetectHigherOrderRepeat(oneMonomer);

        Assert.Multiple(() =>
        {
            Assert.That(result.HasHigherOrderStructure, Is.False);
            Assert.That(result.MonomersPerUnit, Is.EqualTo(1));
            Assert.That(result.MonomerCount, Is.EqualTo(1));
        });
    }

    [Test]
    public void DetectHigherOrderRepeat_TrailingPartialMonomer_IsIgnored()
    {
        // M-HOR-EDGE: A 3-monomer unit ×5 plus a 50-bp partial trailing monomer is split into 15 full
        // monomers; the partial tail is dropped. Period and copy number are unchanged from M-HOR-1.
        string a = Monomer(SubsA), b = Monomer(SubsB), c = Monomer(SubsC);
        string unit = Concat(a, b, c);
        string array = string.Concat(Enumerable.Repeat(unit, 5)) + new string('A', 50);

        var result = ChromosomeAnalyzer.DetectHigherOrderRepeat(array);

        Assert.Multiple(() =>
        {
            Assert.That(result.MonomerCount, Is.EqualTo(15), "Trailing 50-bp partial monomer is ignored");
            Assert.That(result.MonomersPerUnit, Is.EqualTo(3));
            Assert.That(result.HorCopyNumber, Is.EqualTo(5));
        });
    }

    [Test]
    public void DetectHigherOrderRepeat_NonAcgtTrailingPartialMonomer_IsIgnored()
    {
        // M-HOR-NONACGT: A 3-monomer unit ×5 followed by an 80-bp run of non-ACGT 'N' bases. The N run
        // is shorter than one 171-bp monomer, so it forms a trailing partial monomer that is dropped by
        // the floor split (Length/monomerLength). Period, copy number and the identities are therefore
        // identical to the clean M-HOR-1 array — the non-ACGT tail cannot affect a HOR built only from
        // the 15 full monomers preceding it. (Sequences are not required to be pure ACGT; the aligner
        // simply treats N as a non-matching residue, and here it never reaches a full monomer.)
        string a = Monomer(SubsA), b = Monomer(SubsB), c = Monomer(SubsC);
        string unit = Concat(a, b, c);
        string array = string.Concat(Enumerable.Repeat(unit, 5)) + new string('N', 80);

        var result = ChromosomeAnalyzer.DetectHigherOrderRepeat(array);

        Assert.Multiple(() =>
        {
            Assert.That(result.MonomerCount, Is.EqualTo(15),
                "2645 bp / 171 bp = 15 full monomers; the 80-bp non-ACGT tail is a dropped partial monomer");
            Assert.That(result.MonomersPerUnit, Is.EqualTo(3), "Period unchanged by the ignored N tail");
            Assert.That(result.HorCopyNumber, Is.EqualTo(5));
            Assert.That(result.HasHigherOrderStructure, Is.True);
            Assert.That(result.MeanInterHorIdentity, Is.EqualTo(100.0).Within(1e-9),
                "The 15 full monomers are exact HOR copies ⇒ 100% inter-HOR identity");
        });
    }

    [Test]
    public void DetectHigherOrderRepeat_InvalidMonomerLength_Throws()
    {
        Assert.Throws<ArgumentOutOfRangeException>(
            () => ChromosomeAnalyzer.DetectHigherOrderRepeat("ACGTACGT", monomerLength: 0));
    }

    [Test]
    public void DetectHigherOrderRepeat_MixedCaseInput_MatchesUppercase()
    {
        string a = Monomer(SubsA), b = Monomer(SubsB), c = Monomer(SubsC);
        string unit = Concat(a, b, c);
        string array = string.Concat(Enumerable.Repeat(unit, 5));

        var upper = ChromosomeAnalyzer.DetectHigherOrderRepeat(array);
        var lower = ChromosomeAnalyzer.DetectHigherOrderRepeat(array.ToLowerInvariant());

        Assert.Multiple(() =>
        {
            Assert.That(lower.MonomersPerUnit, Is.EqualTo(upper.MonomersPerUnit));
            Assert.That(lower.HorCopyNumber, Is.EqualTo(upper.HorCopyNumber));
            Assert.That(lower.MeanInterHorIdentity, Is.EqualTo(upper.MeanInterHorIdentity).Within(1e-9));
            Assert.That(lower.MeanIntraHorIdentity, Is.EqualTo(upper.MeanIntraHorIdentity).Within(1e-9));
        });
    }

